package com.model;

public class BalanceRecord {
    int id;
    String uid;
    String order_id;
    String time;
    String tikuan_number;
    String tikuan_state;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getOrder_id() {
		return order_id;
	}
	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getTikuan_number() {
		return tikuan_number;
	}
	public void setTikuan_number(String tikuan_number) {
		this.tikuan_number = tikuan_number;
	}
	public String getTikuan_state() {
		return tikuan_state;
	}
	public void setTikuan_state(String tikuan_state) {
		this.tikuan_state = tikuan_state;
	}
    
    
}
